print("\n== Operaciones Aritméticas ==")

x = float(input("Ingresa el primer número: "))
y = float(input("Ingresa el segundo número: "))

print("\n== Resultados ==")
print("Suma:", x + y)
print("Resta:", x - y)
print("Multiplicación:", x * y)
print("División:", x / y)
print("División entera:", x // y)
print("Módulo:", x % y)
print("Potencia:", x ** y)
